namespace App\Support;

use Locale;
use ResourceBundle;

class CountryOptions
{
    public static function all(): array
    {
        $bundle = ResourceBundle::create('en', 'ICUDATA-region');
        $regions = $bundle?->get('Countries');
        $countries = [];

        if ($regions instanceof ResourceBundle) {
            foreach ($regions as $code => $name) {
                if (preg_match('/^[A-Z]{2}$/', (string) $code) === 1) {
                    $countries[$code] = (string) $name;
                }
            }
        }

        if ($countries === []) {
            foreach (self::fallbackCodes() as $code) {
                $countries[$code] = Locale::getDisplayRegion('-'.$code, 'en') ?: $code;
            }
        }

        asort($countries, SORT_NATURAL | SORT_FLAG_CASE);

        return $countries;
    }

